    private void jLabel24MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel24MouseClicked
        // TODO add your handling code here: String query="select * from BOOK";
         String query="select * from ISSUEBOOK where userID=?";
         String search=userSearch.getText();
        try  {
            
             pst =conn.prepareStatement(query);
             pst.setString(1, search);
             rs=pst.executeQuery();
            
            while(rs.next()){
            String Bid=rs.getString("bookID");
            String Bname=rs.getString("bookName");
            String author=rs.getString("author");
            String Price=String.valueOf(rs.getInt("price"));
            String Gdate=rs.getString("getDate");
            String Ddate=rs.getString("dueDate");
          
            
            String tbData[] ={Bid,Bname,author,Price,Gdate,Ddate};
            
            DefaultTableModel tblModel=(DefaultTableModel)returntable.getModel();
            
            tblModel.addRow(tbData);
            
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(manageBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
                
                

    
    }//GEN-LAST:event_jLabel24MouseClicked

