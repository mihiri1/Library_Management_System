    private void CalcMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CalcMouseClicked
            try{ 
            SimpleDateFormat simpleDF=new SimpleDateFormat("MM/dd/yyyy",Locale.ENGLISH);
             Date firstDate =dueDate.getDate();
             Date secondDate=rDate.getDate();
             
             long diff=Math.abs(secondDate.getTime() - firstDate.getTime());
             long ndiff= TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS);
             
             lateDays.setText(String.valueOf(ndiff));
             
             if(ndiff>14){
              
               long total=ndiff*2;
               fine.setText(String.valueOf(total));
             
             }
             else{
               fine.setText("No fine");
             }
            }catch(Exception e){
            
                e.printStackTrace();
            
            }
            
    }//GEN-LAST:event_CalcMouseClicked

